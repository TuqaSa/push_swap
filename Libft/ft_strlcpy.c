#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	lsrc;

	lsrc = ft_strlen(src);
	if (size > lsrc + 1)
		ft_memcpy(dst, src, lsrc + 1);
	else if (size != 0)
	{
		ft_memcpy(dst, src, size - 1);
		dst[size - 1] = 0;
	}
	return (lsrc);
}
